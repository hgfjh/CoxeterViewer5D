def build_quotient_complex(
    request: dict[str, Any],
    source: dict[str, Any],
    input_hash: str,
) -> dict[str, Any]:
    if any(entry == "inf" for row in source["coxeterMatrix"] for entry in row):
        return {
            "ok": True,
            "status": "skipped",
            "backend": BACKEND_ID,
            "backendVersion": BACKEND_VERSION,
            "checkedAt": CHECKED_AT,
            "inputHash": input_hash,
            "warnings": [
                "Native Sage quotient export in this app currently supports finite Coxeter systems. Infinite finite-index subgroup enumeration needs a separate certified Sage task."
            ],
        }

    reflections = build_reflection_matrices(source, sage_imports())
    group = enumerate_finite_group(source, reflections)
    subgroup = enumerate_subgroup(request, reflections, group)
    coset_data = build_left_cosets(group, subgroup)
    errors = [
        *(["finite group enumeration hit the element cap"] if group["capped"] else []),
        *(["subgroup enumeration hit the element cap"] if subgroup["capped"] else []),
        *subgroup["errors"],
        *coset_data["errors"],
    ]
    max_cosets = request.get("maxCosets", DEFAULT_MAX_COSETS)
    if len(coset_data["cosets"]) > max_cosets:
        errors.append(
            f"quotient has {len(coset_data['cosets'])} cosets, exceeding maxCosets {max_cosets}"
        )
    if errors:
        raise ValueError("; ".join(errors))

    vertices = []
    for coset in coset_data["cosets"]:
        representative = group["elements"][coset["representativeIndex"]]
        vertices.append(
            {
                "id": coset["id"],
                "label": coset["id"],
                "representativeWord": representative["word"],
                "sourceNodeIds": [group["elements"][index]["id"] for index in coset["memberIndices"]],
            }
        )

    action_images_by_generator: list[dict[str, str]] = []
    actions = []
    for generator, reflection in enumerate(reflections):
        images: dict[str, str] = {}
        for coset in coset_data["cosets"]:
            representative = group["elements"][coset["representativeIndex"]]
            target_matrix = multiply(representative["matrix"], reflection)
            target_element = group["keyToIndex"].get(matrix_tuple_key(target_matrix))
            if target_element is None:
                raise ValueError(f"generator {generator} action left the enumerated group")
            target_coset = coset_data["elementCoset"][target_element]
            if target_coset < 0:
                raise ValueError(f"generator {generator} action left the enumerated quotient at {coset['id']}")
            images[coset["id"]] = coset_data["cosets"][target_coset]["id"]
        action_images_by_generator.append(images)
        actions.append({"generator": generator, "images": images})

    edges = []
    for coset in coset_data["cosets"]:
        for generator in range(source["rank"]):
            target = action_images_by_generator[generator][coset["id"]]
            edges.append(
                {
                    "id": edge_id(coset["id"], target, generator),
                    "source": coset["id"],
                    "target": target,
                    "generator": generator,
                    "inverseEdgeId": edge_id(target, coset["id"], generator),
                    "label": source["generators"][generator].get("label", f"s{generator}"),
                }
            )

    cells = []
    seen_cells: set[str] = set()
    for i, j, m in finite_pairs(source["coxeterMatrix"]):
        for vertex in vertices:
            boundary_vertices: list[str] = []
            boundary_edges: list[str] = []
            current = vertex["id"]
            for step in range(2 * m):
                generator = i if step % 2 == 0 else j
                target = action_images_by_generator[generator][current]
                boundary_vertices.append(current)
                boundary_edges.append(edge_id(current, target, generator))
                current = target
            if current != vertex["id"]:
                raise ValueError(f"rank-two relation ({i}, {j}) did not close at {vertex['id']}")
            key = f"{i}-{j}:{canonical_boundary_key(boundary_vertices)}"
            if key in seen_cells:
                continue
            seen_cells.add(key)
            cells.append(
                {
                    "id": f"qcell:{i}-{j}:{len(cells)}",
                    "generatorPair": [i, j],
                    "m": m,
                    "boundaryVertexIds": boundary_vertices,
                    "boundaryEdgeIds": boundary_edges,
                }
            )

    warning = (
        "Quotient was produced by native Sage finite left-coset enumeration from the request file. "
        "Infinite or large-index subgroup enumeration requires a separate Sage/GAP certificate."
    )
    certificate = {
        "status": "passed",
        "backend": BACKEND_ID,
        "backendVersion": BACKEND_VERSION,
        "scopes": ["quotient-action"],
        "command": "sage -c runpy.run_path('scripts/sage_quotient_export.py') --input <request>",
        "checkedAt": CHECKED_AT,
        "inputHash": input_hash,
        "diagnostics": {
            "groupElements": len(group["elements"]),
            "subgroupElements": len(subgroup["elements"]),
            "cosets": len(vertices),
            "finitePairCells": len(cells),
            "cosetConvention": "left cosets H\\W with right multiplication by Coxeter generators",
            "deduplication": "sage algebraic-real reflection matrix keys",
        },
        "warnings": [warning],
    }

    quotient: dict[str, Any] = {
        "schemaVersion": 1,
        "name": (
            f"{source['name']} quotient ({request.get('subgroupName')})"
            if request.get("subgroupName")
            else f"{source['name']} quotient from build request"
        ),
        "sourceSystem": source,
        "generatorRank": source["rank"],
        "permutationAction": actions,
        "vertices": vertices,
        "edges": edges,
        "twoCells": cells,
        "subgroup": {
            "name": request.get("subgroupName", "request subgroup"),
            "index": len(vertices),
            "generators": request.get("subgroupGenerators", []),
            "source": BACKEND_ID,
            "certificate": certificate,
            "notes": request.get("notes", []),
        },
        "verifier": certificate,
        "schreierCertificate": {
            "status": "passed",
            "method": "external-sage",
            "checkedAt": CHECKED_AT,
            "generatorRank": source["rank"],
            "vertexCount": len(vertices),
            "checks": {
                "generatorRegularity": True,
                "bijectiveActions": True,
                "involutiveGenerators": True,
                "edgeCompatibility": True,
                "coxeterRelations": True,
                "rankTwoCellCoverage": True,
                "duplicateRankTwoCells": True,
            },
            "rankTwoOrbits": [
                {
                    "generatorPair": cell["generatorPair"],
                    "m": cell["m"],
                    "orbitKey": f"{','.join(str(item) for item in cell['generatorPair'])}:{'>'.join(cell['boundaryVertexIds'])}",
                    "boundaryVertexIds": cell["boundaryVertexIds"],
                    "matchedCellIds": [cell["id"]],
                }
                for cell in cells
            ],
            "errors": [],
            "warnings": [warning],
        },
        "warnings": [warning],
    }
    game = attach_game(request, source, input_hash)
    if game:
        quotient["game"] = game

    hash_source = json.dumps(quotient, sort_keys=True, separators=(",", ":"))
    output_hash = sha256_text(hash_source)
    quotient["verifier"]["outputHash"] = output_hash
    quotient["subgroup"]["certificate"]["outputHash"] = output_hash
    if quotient.get("game", {}).get("cocycles"):
        certificate_block = quotient["game"]["cocycles"][0].get("certificate")
        if certificate_block:
            certificate_block["outputHash"] = output_hash

    return quotient


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--input", type=Path)
    parser.add_argument("--output", type=Path)
    parser.add_argument("--check-runtime", action="store_true")
    args = parser.parse_args()

    if args.check_runtime:
        print_json(sage_runtime_status())
        return 0

    input_text = args.input.read_text(encoding="utf8") if args.input else "{}"
    if sage_runtime_status()["ok"] is not True:
        report = skipped_report(args.input, input_text)
        text = json.dumps(report, indent=2, sort_keys=True) + "\n"
        if args.output:
            args.output.write_text(text, encoding="utf8")
        else:
            print(text, end="")
        return 0

    if args.input is None:
        parser.error("--input is required for quotient export")

    try:
        request = load_json(args.input)
        source = validate_request(request)
        result = build_quotient_complex(request, source, sha256_text(input_text))
    except Exception as exc:
        print_json(
            {
                "ok": False,
                "status": "failed",
                "backend": BACKEND_ID,
                "backendVersion": BACKEND_VERSION,
                "checkedAt": CHECKED_AT,
                "inputPath": stable_path_label(args.input),
                "inputHash": sha256_text(input_text),
                "errors": [str(exc)],
            }
        )
        return 1

    text = json.dumps(result, indent=2, sort_keys=True) + "\n"
    if args.output:
        args.output.write_text(text, encoding="utf8")
    else:
        print(text, end="")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
