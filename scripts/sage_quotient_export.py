#!/usr/bin/env python3
"""Sage quotient exporter for finite Coxeter subgroup/coset data.

The script is intentionally narrow and deterministic.  When run inside Sage it
enumerates a finite Coxeter group with Sage algebraic-real reflection matrices,
enumerates the subgroup generated by the requested words, builds left cosets
``H\\W`` with right multiplication by Coxeter generators, and emits the
``QuotientComplex`` JSON used by the app.  When Sage is not importable it emits
a skipped-status artifact instead of pretending an external certificate exists.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import sys
from pathlib import Path
from typing import Any

sys.path.insert(0, str(Path(__file__).resolve().parent))

from sage_export_backend import (
    build_reflection_matrices,
    sage_imports,
    stable_path_label,
)


BACKEND_ID = "sageQuotientExportBackend"
BACKEND_VERSION = "2.0.0"
CHECKED_AT = "1970-01-01T00:00:00.000Z"
MAX_GROUP_ELEMENTS = 50_000
DEFAULT_MAX_COSETS = 256


def print_json(value: dict[str, Any]) -> None:
    print(json.dumps(value, indent=2, sort_keys=True))


def sha256_text(text: str) -> str:
    return hashlib.sha256(text.encode("utf8")).hexdigest()


def load_json(path: Path) -> dict[str, Any]:
    with path.open("r", encoding="utf8") as handle:
        value = json.load(handle)
    if not isinstance(value, dict):
        raise ValueError("input JSON must be an object")
    return value


def sage_runtime_status() -> dict[str, Any]:
    try:
        import sage.all  # type: ignore  # noqa: F401
    except Exception as exc:  # pragma: no cover - local tool dependent
        return {
            "ok": False,
            "status": "skipped",
            "backend": BACKEND_ID,
            "backendVersion": BACKEND_VERSION,
            "requiredRuntime": "SageMath",
            "checkedAt": CHECKED_AT,
            "warnings": [
                "SageMath is not importable in this Python runtime; native Sage quotient enumeration was skipped."
            ],
            "detail": str(exc),
        }

    return {
        "ok": True,
        "status": "available",
        "backend": BACKEND_ID,
        "backendVersion": BACKEND_VERSION,
        "requiredRuntime": "SageMath",
        "checkedAt": CHECKED_AT,
    }


def skipped_report(input_path: Path | None, input_text: str) -> dict[str, Any]:
    status = sage_runtime_status()
    return {
        "ok": True,
        "status": "skipped",
        "backend": BACKEND_ID,
        "backendVersion": BACKEND_VERSION,
        "checkedAt": CHECKED_AT,
        "inputPath": stable_path_label(input_path) if input_path else None,
        "inputHash": sha256_text(input_text),
        "warnings": status["warnings"],
    }


def is_int(value: Any) -> bool:
    return isinstance(value, int) and not isinstance(value, bool)


def validate_request(request: dict[str, Any]) -> dict[str, Any]:
    errors: list[str] = []
    source = request.get("sourceSystem")
    if not isinstance(source, dict):
        errors.append("sourceSystem must be an object")
    else:
        rank = source.get("rank")
        matrix = source.get("coxeterMatrix")
        generators = source.get("generators")
        if source.get("schemaVersion") != 1:
            errors.append("sourceSystem.schemaVersion must be 1")
        if not is_int(rank) or rank < 1:
            errors.append("sourceSystem.rank must be a positive integer")
        if not isinstance(generators, list) or len(generators) != rank:
            errors.append("sourceSystem.generators must have rank entries")
        if not isinstance(matrix, list) or len(matrix) != rank:
            errors.append("sourceSystem.coxeterMatrix must be rank by rank")
        elif is_int(rank):
            for i, row in enumerate(matrix):
                if not isinstance(row, list) or len(row) != rank:
                    errors.append(f"sourceSystem.coxeterMatrix[{i}] must have rank entries")
                    continue
                for j, entry in enumerate(row):
                    if i == j:
                        if entry != 1:
                            errors.append(f"sourceSystem.coxeterMatrix[{i}][{j}] must be 1")
                    elif entry != "inf" and (not is_int(entry) or entry < 2):
                        errors.append(
                            f"sourceSystem.coxeterMatrix[{i}][{j}] must be an integer >= 2 or 'inf'"
                        )
                    elif isinstance(matrix[j], list) and matrix[j][i] != entry:
                        errors.append(f"sourceSystem.coxeterMatrix must be symmetric at ({i}, {j})")

    subgroup_words = request.get("subgroupGenerators")
    if not isinstance(subgroup_words, list):
        errors.append("subgroupGenerators must be an array of words")
    else:
        rank = source.get("rank") if isinstance(source, dict) else None
        for word_index, word in enumerate(subgroup_words):
            if not isinstance(word, list):
                errors.append(f"subgroupGenerators[{word_index}] must be a word array")
                continue
            for letter_index, generator in enumerate(word):
                if (
                    not is_int(generator)
                    or generator < 0
                    or (is_int(rank) and generator >= rank)
                ):
                    errors.append(
                        f"subgroupGenerators[{word_index}][{letter_index}] must be a valid generator index"
                    )

    max_cosets = request.get("maxCosets", DEFAULT_MAX_COSETS)
    if not is_int(max_cosets) or max_cosets < 1:
        errors.append("maxCosets must be a positive integer when provided")

    if errors:
        raise ValueError("; ".join(errors))
    return source


def matrix_tuple_key(matrix_value: Any) -> tuple[Any, ...]:
    return tuple(matrix_value.list())


def identity_matrix_for(rank: int) -> Any:
    from sage.all import AA, identity_matrix  # type: ignore

    return identity_matrix(AA, rank)


def multiply(left: Any, right: Any) -> Any:
    return left * right


def node_id_from_word(word: list[int]) -> str:
    return "e" if not word else f"w:{'.'.join(str(item) for item in word)}"


def edge_id(source: str, target: str, generator: int) -> str:
    return (
        f"qe:{source}:s{generator}"
        if source == target
        else f"qe:{source}:{target}:s{generator}"
    )


def finite_pairs(matrix: list[list[Any]]) -> list[tuple[int, int, int]]:
    pairs: list[tuple[int, int, int]] = []
    for i in range(len(matrix)):
        for j in range(i + 1, len(matrix)):
            entry = matrix[i][j]
            if is_int(entry):
                pairs.append((i, j, entry))
    return pairs


def canonical_boundary_key(boundary: list[str]) -> str:
    rotations: list[str] = []
    for index in range(len(boundary)):
        rotations.append(">".join(boundary[index:] + boundary[:index]))
    reversed_boundary = list(reversed(boundary))
    for index in range(len(reversed_boundary)):
        rotations.append(
            ">".join(reversed_boundary[index:] + reversed_boundary[:index])
        )
    return sorted(rotations)[0] if rotations else ""


def word_matrix(word: list[int], reflections: list[Any]) -> Any:
    matrix = identity_matrix_for(len(reflections))
    for generator in word:
        matrix = multiply(matrix, reflections[generator])
    return matrix


def enumerate_finite_group(source: dict[str, Any], reflections: list[Any]) -> dict[str, Any]:
    identity = identity_matrix_for(source["rank"])
    elements = [
        {
            "id": "e",
            "word": [],
            "matrix": identity,
            "key": matrix_tuple_key(identity),
        }
    ]
    key_to_index = {elements[0]["key"]: 0}
    capped = False

    cursor = 0
    while cursor < len(elements):
        element = elements[cursor]
        cursor += 1
        for generator, reflection in enumerate(reflections):
            matrix = multiply(element["matrix"], reflection)
            key = matrix_tuple_key(matrix)
            if key in key_to_index:
                continue
            if len(elements) >= MAX_GROUP_ELEMENTS:
                capped = True
                continue
            word = [*element["word"], generator]
            key_to_index[key] = len(elements)
            elements.append(
                {
                    "id": node_id_from_word(word),
                    "word": word,
                    "matrix": matrix,
                    "key": key,
                }
            )

    return {"elements": elements, "keyToIndex": key_to_index, "capped": capped}


def enumerate_subgroup(
    request: dict[str, Any],
    reflections: list[Any],
    group: dict[str, Any],
) -> dict[str, Any]:
    subgroup_matrices: list[Any] = []
    for word in request.get("subgroupGenerators", []):
        subgroup_matrices.append(word_matrix(word, reflections))
        subgroup_matrices.append(word_matrix(list(reversed(word)), reflections))

    identity = identity_matrix_for(len(reflections))
    elements = [{"matrix": identity, "key": matrix_tuple_key(identity), "word": []}]
    seen = {elements[0]["key"]}
    errors: list[str] = []
    capped = False

    cursor = 0
    while cursor < len(elements):
        if not subgroup_matrices:
            break
        element = elements[cursor]
        cursor += 1
        for generator_matrix in subgroup_matrices:
            matrix = multiply(element["matrix"], generator_matrix)
            key = matrix_tuple_key(matrix)
            if key not in group["keyToIndex"]:
                errors.append(
                    "subgroup enumeration reached an element outside the enumerated finite group"
                )
                continue
            if key in seen:
                continue
            if len(elements) >= MAX_GROUP_ELEMENTS:
                capped = True
                continue
            seen.add(key)
            elements.append({"matrix": matrix, "key": key, "word": []})

    return {"elements": elements, "keys": sorted(str(key) for key in seen), "errors": errors, "capped": capped}


def build_left_cosets(group: dict[str, Any], subgroup: dict[str, Any]) -> dict[str, Any]:
    element_coset = [-1 for _ in group["elements"]]
    cosets: list[dict[str, Any]] = []
    errors: list[str] = []

    for index, representative in enumerate(group["elements"]):
        if element_coset[index] >= 0:
            continue
        member_indices: set[int] = set()
        for subgroup_element in subgroup["elements"]:
            matrix = multiply(subgroup_element["matrix"], representative["matrix"])
            key = matrix_tuple_key(matrix)
            member_index = group["keyToIndex"].get(key)
            if member_index is None:
                errors.append("left-coset construction found an element outside the enumerated group")
                continue
            member_indices.add(member_index)
        coset_index = len(cosets)
        for member_index in member_indices:
            if element_coset[member_index] not in (-1, coset_index):
                errors.append("left-coset construction produced overlapping cosets")
            element_coset[member_index] = coset_index
        cosets.append(
            {
                "id": f"q{coset_index}",
                "representativeIndex": index,
                "memberIndices": sorted(member_indices),
            }
        )

    return {"cosets": cosets, "elementCoset": element_coset, "errors": errors}


